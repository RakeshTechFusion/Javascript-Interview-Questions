//definition of NaN
//NaN is a property of the global object. In other words, it is a variable in global scope. The initial value of NaN is Not-A-Number - the same as the value of Number.NaN. It is a special value that represents "not a number". This property is useful in cases where a function should return a numeric value but cannot produce one. For example, Math.sqrt(-1) returns NaN because the square root of -1 is not a real number. (All square roots of negative numbers are complex numbers.)
